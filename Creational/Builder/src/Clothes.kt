class Clothes {

    private var sewingTreads = ""
    private var textile = "none"
    private var sewingButtons = ""

    fun setSewingThreads(sewingTreads: String){
        this.sewingTreads = sewingTreads
    }

    fun setTextile(textile: String){
        this.textile = textile
    }
    fun setSewingButtons(sewingButtons: String){
        this.sewingButtons = sewingButtons
    }

}